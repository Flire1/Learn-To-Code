# Level 1, Part 3: Math

# + is addition
# - is subtraction
# * is multiplication
# / is division

# Variable Assignment
a = 10  # Assigning the number 10 to the variable 'a'
b = 5   # Assigning the number 5 to the variable 'b'

# 1. Addition
answer = a + b  # Storing the result of addition in 'answer'
print(f"Answer of a + b: {answer}") # Output: Answer of a + b: 15
print(f"{a} + {b} = {a + b}") # Output: 10 + 5 = 15
print(a + b) # Output: 15
print(answer) # Output: 15

# 2. Addition with Explanation
print(f"Addition: {a} + {b} = {a + b}") # Output: Addition: 10 + 5 = 15

# 3. Subtraction
print(f"Subtraction: {a} - {b} = {a - b}") # Output: Subtraction: 10 - 5 = 5

# 4. Multiplication
print(f"Multiplication: {a} * {b} = {a * b}") # Output: Multiplication: 10 * 5 = 50

# 5. Division
print(f"Division: {a} / {b} = {a / b}") # Output: Division: 10 / 5 = 2.0

